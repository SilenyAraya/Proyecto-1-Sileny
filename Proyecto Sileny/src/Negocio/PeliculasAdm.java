    private void btnAgragarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgragarPActionPerformed
        ////The information entered is added to the table
        String [] Datos= new String[3];
        Datos[0]=txtxGeneroP.getText();
        txtxGeneroP.setText(null);
        Datos[1]=txtxAutor.getText();
        txtxAutor.setText(null);
        Datos[2] = txtPeli.getText();
        txtCantPeli.setText(null);
        Datos[3] = txtCantPeli.getText();
        txtCantPeli.setText(null);
        modeloPelicula.addRow(Datos);

        String gen = txtxGeneroP.getText();
        String aut = txtxAutor.getText();
        String nomPeli = txtPeli.getText();
        String cantDis = txtCantPeli.getText();
        pelicula nuevaPelicula = new pelicula(gen, aut, nomPeli, Integer.parseInt(cantDis));
        nuevaPelicula.agregarPelicula();

        JOptionPane.showMessageDialog(null, "Se agrego la pelicula correctamente");
    }//GEN-LAST:event_btnAgragarPActionPerformed

