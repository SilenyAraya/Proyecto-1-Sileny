    private void btnModiPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModiPActionPerformed
        //A row is selected to modify it
        int FilaSelec= tablaPelis.getSelectedRow();
        if (FilaSelec >= 0){
            txtxGeneroP.setText(tablaPelis.getValueAt(FilaSelec, 0).toString());
            txtxAutor.setText(tablaPelis.getValueAt(FilaSelec, 1).toString());
            txtPeli.setText(tablaPelis.getValueAt(FilaSelec, 2).toString());
            txtCantPeli.setText(tablaPelis.getValueAt(FilaSelec, 3).toString());
        }else
        //If none is selected, it is notified
            JOptionPane.showMessageDialog(this, "Fila no selecciona");
    }//GEN-LAST:event_btnModiPActionPerformed

    private void btnEliPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliPActionPerformed
        // A row is selected to remove it
        int FilaSelecEli = tablaPelis.getSelectedRow();
        if (FilaSelecEli >= 0){
            modeloPelicula.removeRow(FilaSelecEli);
            
        }else
            //If none is selected, it is notified
            JOptionPane.showMessageDialog(this, "Fila no selecciona");
    }//GEN-LAST:event_btnEliPActionPerformed

